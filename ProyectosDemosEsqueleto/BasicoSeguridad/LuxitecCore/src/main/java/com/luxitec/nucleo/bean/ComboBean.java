package com.luxitec.nucleo.bean;


public class ComboBean {

	private String key;
	private String value;
	private boolean boolSelect;
	private Object objetoInstancia;
	
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public boolean isBoolSelect() {
		return boolSelect;
	}
	public void setBoolSelect(boolean boolSelect) {
		this.boolSelect = boolSelect;
	}
	public Object getObjetoInstancia() {
		return objetoInstancia;
	}
	public void setObjetoInstancia(Object objetoInstancia) {
		this.objetoInstancia = objetoInstancia;
	}

	
}
